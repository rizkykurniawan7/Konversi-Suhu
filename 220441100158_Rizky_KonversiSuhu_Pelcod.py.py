print("1. Celcius")
c = 27
print("hasil dari celcius ke fahrenhait adalah=", 9/5*c+32)
print("hasil dari celcius ke reamur adalah=", 4/5*c)
print("hasil dari celcius ke kelvin adalah=", c+273)

print("2. Fahrenhait")
f = 65
print("hasil dari fahrenhait ke celcius adalah=", 5/9*(f-32))
print("hasil dari fahrenhait ke reamur adalah=", 4/9*(f-32))
print("hasil dari fahrenhait ke kelvin adalah=", f)

print("3. Reamur")
r = 80
print("hasil dari reamur ke celcius adalah=", 5/4*r)
print("hasil dari reamur ke fahrenhait adalah=", 9/4*r+32)
print("hasil dari reamur ke kelvin adalah=", 5/4*r+273)

print("4. Kelvin")
k = 85
print("hasil dari kelvin ke celcius adalah=", k-273)
print("hasil dari kelvin ke fahrenhait adalah=", k)
print("hasil dari kelvin ke reamur adalah=", 4/5*(k-273))


